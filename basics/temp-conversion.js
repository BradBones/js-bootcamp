let fahrenheit = 50; // 0c - 273.15k

// calculate cesius and store in celsius variable
let celsius = (fahrenheit - 32) / 1.8;
// print value
console.log(`${fahrenheit} degrees F = ${celsius} degrees C`);

// calculate kelvin value and store in variable
let kelvin = (fahrenheit + 459.67) / 1.8;
// print that value
console.log(`${fahrenheit} degrees F = ${kelvin} degrees K`);

